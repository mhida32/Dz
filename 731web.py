from math import sqrt, pi
print("Длины сторон треугольника:")
a = float(input("a = "))
b = float(input("b = "))
c = float(input("c = "))
if a > 0 and b>0 and c>0 and a + b > c or a+c >b or b+c>a:
 p = (a + b + c) / 2
 s = sqrt(p * (p - a) * (p - b) * (p - c))
else:
 print("error")
print("Площадь: %.2f" % s)
